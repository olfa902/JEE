package Beans;

public class RegisterBean {
	
		String nom;
		String prenom;

		    public RegisterBean() {
		        nom="";
		        prenom="";
		        
		    }

		    public String getNom() {
		        return nom;
		    }

		    public void setNom(String nom) {
		        this.nom = nom;
		    }

		    public String getPrenom() {
		        return prenom;
		    }

		    public void setPrenom(String prenom) {
		        this.prenom = prenom;
		    }
		    
		    
}
